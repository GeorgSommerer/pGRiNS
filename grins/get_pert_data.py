def steadystate_to_adata(path_to_data : str, zero_cutoff : int):
    """
    Takes a .parquet file containing the steady states generated by GRiNS-Racipe and turns it into an AnnData object.
    Do note that the steady state entries in grins_data.X are not discrete. 

    Parameters:
    -----------
    path_to_data : str
        A path to the file name of the data generated by GRiNS, without the name extension (.parquet, .csv, .h5ad, ...).
    zero_cutoff : float
        A threshold where steady state concentrations below this value will be set to 0.

    Returns:
    --------
    grins_data : anndata
        An AnnData object containing the GRiNS steady state solutions.
    """

    print(f"Read {path_to_data}.parquet...")
    sol_df = pd.read_parquet(f"{path_to_data}.parquet")
    
    # Create the AnnData object:
    print("Create AnnData object...")
    gk_cols = [col for col in sol_df.columns if "gk_" in col]
    gene_cols = [col.replace("gk_", "") for col in sol_df.columns if "gk_" in col]
    sol_matrix = sol_df.loc[:,gene_cols].values
    
    for i in range(sol_matrix.shape[0]):
        if i % 100000 == 0:
            print(i)
        for j in range(sol_matrix.shape[1]):
            if sol_matrix[i,j] < zero_cutoff:
                sol_matrix[i,j] = 0
    
    sol_sparse = sparse.csc_matrix(sol_matrix)
    grins_data = ad.AnnData(sol_sparse)
    grins_data.obs_names = [str(sol_df.loc[:,"param_index"][i])+"_"+str(sol_df.loc[:,"ic_index"][i]) for i in range(sol_df.shape[0])]
    grins_data.var_names = gene_cols
    return grins_data


