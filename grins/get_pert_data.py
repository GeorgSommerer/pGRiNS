def subset_datasets(grn_df, adata):
    """
    Takes a GRN and an anndata object as inputs, and removes all genes that do not appear in both of them.
    """

    grn_genes = set(list(grn_df["Source"])+list(grn_df["Target"]))
    adata_genes = set(list(adata.var.index))
    common_genes = grn_genes & adata_genes

    grn_df_subset = grn_df.loc(grn_df["Source"] in common_genes)
    grn_df_subset = grn_df.loc(grn_df["Target"] in common_genes)
    adata_subset = adata[:,list(common_genes)]
    return grn_df_subset, adata_subset

